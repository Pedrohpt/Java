/*
 10.	Calcular e exibir o valor do perímetro de um retângulo. O perímetro é calculado pela fórmula: perímetro = 2 * lado 01  + 2 * lado 02.
*/
public class Atividade10 {
    public static void main(String[] args) {
        double lado1 = 5;
        double lado2 = 9;
        double perimetro = ((2*lado1) + (2*lado2));

        System.out.println("O perímetro do retângulo é: " + perimetro);
    }
    
}
